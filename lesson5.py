# 1. Создать программно файл в текстовом формате, записать в него построчно данные, вводимые пользователем.
# Об окончании ввода данных свидетельствует пустая строка.

my_file = open('my_file.txt', 'w')        # Создать программно файл в текстовом формате

while True:                               # записать в него построчно данные, вводимые пользователем
    user_text = input('Введите строку: ')
    if user_text == '':                        # Об окончании ввода данных свидетельствует пустая строка.
        break
    my_file.writelines(user_text + '\n')

my_file.close()


